static const int url_detector_start = 1;





url_detector::url_type url_detector::detect(string_piece str) {
  int cs;
  const char* p = str.str;
  const char* pe = str.str + str.len;

  url_type result = NO_URL;
  
	{
	cs = url_detector_start;
	}

	{
	int _klen;
	const char *_keys;
	int _trans;

	if ( p == pe )
		goto _test_eof;
	if ( cs == 0 )
		goto _out;
_resume:
	_keys = _url_detector_trans_keys + _url_detector_key_offsets[cs];
	_trans = _url_detector_index_offsets[cs];

	_klen = _url_detector_single_lengths[cs];
	if ( _klen > 0 ) {
		const char *_lower = _keys;
		const char *_mid;
		const char *_upper = _keys + _klen - 1;
		while (1) {
			if ( _upper < _lower )
				break;

			_mid = _lower + ((_upper-_lower) >> 1);
			if ( (*p) < *_mid )
				_upper = _mid - 1;
			else if ( (*p) > *_mid )
				_lower = _mid + 1;
			else {
				_trans += (unsigned int)(_mid - _keys);
				goto _match;
			}
		}
		_keys += _klen;
		_trans += _klen;
	}

	_klen = _url_detector_range_lengths[cs];
	if ( _klen > 0 ) {
		const char *_lower = _keys;
		const char *_mid;
		const char *_upper = _keys + (_klen<<1) - 2;
		while (1) {
			if ( _upper < _lower )
				break;

			_mid = _lower + (((_upper-_lower) >> 1) & ~1);
			if ( (*p) < _mid[0] )
				_upper = _mid - 2;
			else if ( (*p) > _mid[1] )
				_lower = _mid + 2;
			else {
				_trans += (unsigned int)((_mid - _keys)>>1);
				goto _match;
			}
		}
		_trans += _klen;
	}

_match:
	_trans = _url_detector_indicies[_trans];
	cs = _url_detector_trans_targs[_trans];

	if ( cs == 0 )
		goto _out;
	if ( ++p != pe )
		goto _resume;
	_test_eof: {}
	if ( p == ( pe) )
	{
	switch ( _url_detector_eof_actions[cs] ) {
	case 1:
	{ result = URL; }
	break;
	case 2:
	{ result = URL; }
	{ result = EMAIL; }
	break;
	}
	}

	_out: {}
	}


  return result;
}

